// Подсчет общего количества позиций
function countTotalPositions() {
    let total = 0;
    Object.keys(priceData).forEach(region => {
        Object.keys(priceData[region]).forEach(size => {
            total += Object.keys(priceData[region][size]).length;
        });
    });
    return total;
}

